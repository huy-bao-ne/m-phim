// Lấy phim theo category
export const getMoviesByCategory = (category) => {
  return moviesData.filter(movie => movie.category === category);
};

// Lấy phim ngẫu nhiên cho Hero Section
export const getRandomMovie = () => {
  const randomIndex = Math.floor(Math.random() * moviesData.length);
  return moviesData[randomIndex];
};

// Tìm kiếm phim theo keyword
export const searchMovies = (keyword) => {
  const lowerKeyword = keyword.toLowerCase();
  return moviesData.filter(movie => 
    movie.title.toLowerCase().includes(lowerKeyword) ||
    movie.description.toLowerCase().includes(lowerKeyword) ||
    movie.genre.some(g => g.toLowerCase().includes(lowerKeyword))
  );
};

